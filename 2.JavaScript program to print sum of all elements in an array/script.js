function arraySum(array) {
  let sum = 0;
  for (let i = 0; i < array.length; i++) {
    sum = sum + array[i];
  }
  console.log("Hello World");
  return sum;
}

const sum = arraySum([2, 6]); //8
console.log(`The sum of an array is ${sum}`); //The sum of an array is 8

